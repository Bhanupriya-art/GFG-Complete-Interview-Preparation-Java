class Solution
{    
    //Function is to check whether two strings are anagram of each other or not.
    public static boolean isAnagram(String a,String b)
    {
        
        // Your code here
        if(a.length() != b.length()){
            return false;
        }
        
        char a1[] = a.toCharArray();
        Arrays.sort(a1);
        a = new String(a1);
        
        char a2[] = b.toCharArray();
        Arrays.sort(a2);
        b =  new String(a2);
        return a.equals(b);
    }
}
